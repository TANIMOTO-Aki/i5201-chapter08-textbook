public class Calc7 {
    /* 2つの値の加算を行うメソッド */
    public int add(int val1, int val2) {
        int answer;
        answer = val1 + val2;
        return answer;
    }
    /* 2つの値の減算を行うメソッド */
    public int sub(int val1,int val2) {
        int answer;
        answer = val1 - val2;
        return answer;
    }
}
